// Keys are lowercase so lookups are case-insensitive: the link card editor
// stores TitleCase values like "Instagram" while the footer's `Label|url`
// format historically uses lowercase ("facebook|https://…"). Normalizing
// both at lookup time lets either source resolve to the same icon.
const PLATFORM_ICONS: Record<string, PlatformIconComponent> = {
  facebook: FacebookIcon,
  instagram: InstagramIcon,
  youtube: YouTubeIcon,
  twitter: TwitterIcon,
  x: XIcon,
  linkedin: LinkedInIcon,
  tiktok: TikTokIcon,
  github: GitHubIcon,
  pinterest: PinterestIcon,
  snapchat: SnapchatIcon,
  whatsapp: WhatsAppIcon,
  zillow: ZillowIcon,
};

export function getPlatformIcon(platform: string): PlatformIconComponent {
  if (!platform) return Globe;
  return PLATFORM_ICONS[platform.toLowerCase().trim()] ?? Globe;
}
